                            指客戶如申請冷靜期內取消的保單為投資保單、一筆過付清保單或即享年金保單，退回之保費需扣除市值損失之調整金額及已給付之年金。<br>
                            <br>
                            <strong>Matured  (保單期滿)</strong><br>
                            保單的有效期屆滿或保單的保障期限已到期，保單效力將會終止。<br>
                            <br>
                            <strong>Maturity  (保單期滿)</strong><br>
                            保單的有效期屆滿或保單的保障期限已到期，保單效力將會終止。<br>
                            <br>
                            <strong>Maturity Date  (到期日 / 期滿日)</strong><br>
