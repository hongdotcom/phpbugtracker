                            <br>
                            <strong>Joint Owner  (聯合帳戶 /  聯名帳戶)</strong><br>
                            由兩個或以上客戶共同申請的一份保單。<br>
                            <br>
                            <span style="text-decoration: underline;"><strong>L</strong></span></p>
                        <p><strong>Lapsed  (保單已失效)</strong><br>
                            當保單帳戶價值不足以支付保單收費，或寬限期內未繳保費，保單會因而不再生效，而所有的保障包括身故賠償及自選附加保障（如有）亦告終止。<br>
                            <br>
