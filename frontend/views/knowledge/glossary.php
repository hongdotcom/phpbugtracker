                            <br>
                            <strong>Life Insurance  (人壽保險)</strong><br>
                            人壽保險是人身保險的一種，簡稱壽險。當受保人的生命發生了保險事故時，由保險公司支付保險金。<br>
                            <br>
                            <strong>Loading  (額外保費 /  附加保費)</strong><br>
                            附加保障所需要支付的額外保費。<br>
                            <br>
                            <strong>Lost of Policy  (遺失保單)</strong><br>
                            客戶遺失保險公司簽發的保單正本。<br>
                            <br>
                            <strong>Lump Sum / Un-Planned Premium / Un-Scheduled Premium  (一次繳清)</strong><br>
                            一次性繳清所有保費。<br>
