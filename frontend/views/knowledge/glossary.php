                            <br>
                            <strong>Partial Surrender  (部分退保)</strong><br>
                            保單過了最初供款期後，客戶可以部分提取保單價值（一般為累積供款帳戶內的價值，不可提取最初供款期內的價值）。<br>
                            <br>
                            <strong>Partial Surrender History  (部分提款紀錄)</strong><br>
                            客戶曾經部分提款的歷史紀錄，通常包括金額、時間等。<br>
                            <br>
                            <strong>Partial Surrender Value  (可部分提款價值)</strong><br>
                            客戶可提取部分的保單價值。<br>
                            <br>
                            <strong>Payment Frequency  (付款頻率)</strong><br>
