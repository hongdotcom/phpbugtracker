                            <strong>Inflation Protector Option - Manulife - IPO  (通脹加保權益 - 宏利)</strong><br>
                            通脤加保權益(IPO)是一項能使保障額免受通脤影響的權益。保單持有人只需於投保時選擇將通脹加保權益附加於保單內，於首十個保單周年日，基本保額會增加原有基本保額的 5%或 10% (視乎基本計劃)而毋需出示受保資格證明。 <br>
                            <br>
                            <strong>Inforce  (生效)</strong><br>
                            保單生效，保險公司承擔保險責任的期間。<br>
                            <br>
                            <strong>Initial Account  (最初供款帳戶)</strong><br>
                            在投資相連壽險計劃中，保險公司將客戶在最初供款期內所繳付的定期保費，用以分配客戶投資選擇的名義單位至最初供款帳戶。<br>
                            <br>
                            <strong>Initial Account Value  (最初供款帳戶價值)</strong><br>
                            在投資相連壽險計劃中，最初供款帳戶內的價值，一般不可提取。<br>
                            <br>
                            <strong>Initial Contribution Period - ICP  (最初供款期)</strong><br>
                            適用於投資相連壽險計劃，時間為保單開始日起計18個月至24個月不等，期間不可減額或中斷供款，否則會有罰金。<br>
                            <br>
                            <strong>Initial Premium  (首期保費)</strong><br>
                            購買保單時，首次為該保單支付的保費。<br>
                            <br>
                            <strong>Initial Units - I Units  (最初供款帳戶內的基金單位)</strong><br>
                            在最初供款帳戶內的基金單位。<br>
                            <br>
                            <strong>Insurable Interest  (可保權益)</strong><br>
                            “可保利益”又稱為“可保權益”，指投保人及受保人在法律上被認可的一種關係，比如受保人生命、身體、財產、法律責任或權利等，會造成投保人經濟損失，這種關係賦予了投保人對該受保人購買保險的權利。<br>
                            <br>
                            <strong>Insurance Authority - IA  (保險業監管局 / 保監局)</strong><br>
                            保險業監管局(保監局)於2015年12月成立，並於2017年6月接替保險業監理處的規管工作。保監局是獨立於政府的新保險業監管機構，成立的目標是確保保險業的規管架構與時並進，促進保險業的穩健發展；為保單持有人提供更佳保障；以及遵行國際保險監督聯會的規定。<br>
                            <br>
                            <strong>Insured  (受保人 / 被保人)</strong><br>
                            保單內列明受保障的人，受保險合同所保障。保单生效后，受保人是不可以更改的。<br>
                            <br>
                            <strong>Intermediary  (中間人)</strong><br>
                            為客戶安排購買保險的人，包括保險代理、保險經紀或理財顧問。<br>
                            <br>
                            <strong>Internal Transfer  (內部轉讓)</strong><br>
                            客戶將其保單的服務權限轉給另一位理財顧問。<br>
