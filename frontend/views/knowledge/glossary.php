                            為讓內地訪客在決定購買前能掌握有關資訊，保監局認為有需要提醒他們在香港投保時應考慮的因素及風險。所有內地訪客的個人長期保單的新申請，都必須包括一份《內地重要資料聲明書--內地人士在港投購人身/壽險保單》。<br>
