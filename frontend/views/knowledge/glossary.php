                        <span style="font-size: 24px; color: #0070c0;"><strong>
                                <p>Glossary&nbsp;常用詞彙</p>
                                <p>&nbsp;</p>
                            </strong></span>
                        <span style="text-decoration: underline;"><strong>
                                <p>A</p>
                            </strong></span>
                        <p>
                            <strong>Account Closure  (終止戶口)</strong><br>
                            當客戶於本公司的戶口已無生效中的保單及/或基金單位，客戶可以書面申請結束本公司的所有戶口，一經執行，客戶於本公司持有的所有戶口將全部結束。<br>
                            <br>
                            <strong>Accumulation Account  (累積供款帳戶 / 增值戶口)</strong><br>
                            投資相連壽險計劃中，保險公司將客戶在最初供款期後所繳付的定期保費，用以分配客戶投資選擇的名義單位至累積供款帳戶。<br>
                            <br>
                            <strong>Accumulation Account Value  (累積供款帳戶價值)</strong><br>
                            投資相連壽險計劃中，最初供款期後帳戶的現金價值結餘，此價值可被提取。<br>
                            <br>
                            <strong>Accumulation Units - A Units  (累積供款帳戶內的基金單位)</strong><br>
                            累積供款帳戶內的基金單位。<br>
                            <br>
                            <strong>Add Rider  (增加附加保障)</strong><br>
                            客戶為現有保單增加附加保障。<br>
                            <br>
                            <strong>Address Proof  (住址證明)</strong><br>
                            水電煤單或電話月結單或銀行對帳單等文件，須包括客戶姓名、居住地址等個人資料。須為最近三個月內發出。<br>
                            <br>
                            <strong>Administration Fee  (行政費用)</strong><br>
                            保險公司用於保單行政管理所收取的費用。<br>
                            <br>
                            <strong>Anniversary Report/ Anniversary Statement  (週年報告 / 年結通知書)</strong><br>
                            保險公司會在保單週年日以信件或電郵方式寄給客戶年結報告，該報告包含保單的價值等資訊。<br>
                            <br>
                            <strong>Annuity Option  (年金計劃選擇)</strong><br>
                            年金計劃是一種向保險公司領取現金的保險計劃，保險公司會收取受領人(Annuitant)一筆款項作為支付年金的代價。年金的受領人可以一個或多於一個，而購買年金的保費可在保單開始時一次過繳付，亦可預先分期繳付。<br>
                            <br>
                            <strong>Arrear  (欠款)</strong><br>
                            保單欠繳保費。<br>
                            <strong><br>
                                Asset Transfer  (資產轉移)</strong><br>
                            一般在MPF中出現，即客戶對強積金戶口內所持資產進行轉移。<br>
                            <br>
                            <strong>Assignee  (受讓人)</strong><br>
                            即新owner，保單轉讓後持有保單的新持有人。<br>
                            <br>
                            <strong>Assignment  (保單轉讓 / 轉名)</strong><br>
                            保單原持有人將保單轉讓給另一人。<br>
                            <br>
                            <strong>Assignor  (轉讓者)</strong><br>
                            即舊owner，保單轉讓前的原持有人。<br>
                            <br>
                            <strong>Autopay  (自動轉賬)</strong><br>
                            客戶授權銀行建立直接付款指示，允許保險公司定期從客戶指定的銀行戶口自動轉賬保費至保險公司（保單）<br>
                            <br>
                            <span style="text-decoration: underline;"><strong>B</strong></span></p>
                        <p><strong>Bank Draft  (銀行匯票)</strong><br>
                            銀行匯票是匯款人將款項存入銀行，再經由銀行代為出票。收費標準視乎每間銀行而定。<br>
                            <br>
                            <strong>Bank Standing Order - BSO  (銀行常行指令)</strong><br>
                            客戶與銀行簽訂的協議，銀行根據此協議定期劃款給某個帳戶（即銀行定期過數至客戶的保單用作交保費），且每次過數款項是固定不變的。 如客戶欲取消銀行常行指示, 須向銀行辦理取消安排。<br>
                            <br>
                            <strong>Basic Plan  (基本計劃 )</strong><br>
                            可以單獨投保的保險險種，每份保單都有一個主險。<br>
                            <br>
                            <strong>Beneficiary  (受益人)</strong><br>
                            保單持有人所指定接受保險賠償金的個人或個體。投保人和/或受保人均可為受益人。如投保人或受保人未指定保單受益人，其法定繼承人即為受益人。<br>
                            <br>
                            <strong>Benefit Protection Option - Prudential - BPO  (遞增保障權益 - 保誠)</strong><br>
                            「遞增保障權益」指客戶毋須提供任何受保資格證明，基本計劃之投保額將於每個保單周年日自動提升5%，以達致抵消通脹目的。保單持有人可自行選擇暫停或終止行使「遞增保障權益」。<br>
                            <strong><br>
                                <strong>Bid Price  (買價)</strong></strong><br>
                            投資者賣出投資選擇單位的價格。<br>
                            <br>
                            <strong>Bonus  (額外獎賞)</strong><br>
                            保單生效達到指定年期後，保險公司將額外派發非保證特別獎賞。<br>
                            <br>
                            <strong>Broker  (經紀公司 / 中介人)</strong><br>
                            一般指本公司，或本公司的理財顧問。<br>
                            <br>
                            <strong>Business Registration - BR  (商業登記證)</strong><br>
                            所有商業機構必須向稅務局轄下的商業登記署辦理商業登記，已登記業務必須繳付登記費以領取商業登記證。<br>
                            <br>
                            <strong>Business Volume - BV</strong> <br>
                            用於計算業績的數據。<br>
                            <br>
                            <strong><span style="text-decoration: underline;">C</span></strong></p>
                        <p><strong>Cancellation Policy  (取消保單)</strong><br>
                            保單尚未完成出單時客戶提出取消申請。<br>
                            <br>
                            <strong>Capital Investment Entrant Scheme - CIES  (資本投資者入境計劃)</strong><br>
                            是香港特别行政區政府於2003年10月27日起實施、至2015年1月15日結束的投資移民計劃，其目的是讓那些把資金帶來香港，但不會在港參與經營任何業務的人士（即投資者）來港居留。在本公司內部使用上則可被理解為透過資本投資者入境計劃的一種客戶類型。<br>
                            <br>
                            <strong>Cash Value  (現金價值)</strong><br>
                            保單持有人在取消保單合約時可以取回之保單價值。<br>
                            <br>
                            <strong>Certified True Copy  (經核證無誤的副本 / 認証真本)</strong><br>
                            證明該文件副本與原件相符，或經核證後的文件。<br>
                            <br>
                            <strong>Certifier  (證明者 / 核證者)</strong><br>
                            見證文件的簽署及文件為有效副本的人。<br>
                            <br>
                            <strong>Change Of Payment Method  (更改付款方式)</strong><br>
                            客戶更改保單的支付方式，例如由現金繳費更改至自動轉賬繳費。<br>
                            <br>
                            <strong>Change Of Servicing Right  (更改服務權限)</strong><br>
                            客戶更改為其服務的理財顧問。<br>
                            <br>
                            <strong>Change Portfolio - DPMS  (更改投資組合)</strong><br>
                            為達到客戶的投資目標，在獲得客戶的全權委託下，投資組合經理會隨時檢討並更改其保單的投資組合。<br>
                            <br>
                            <strong>Cheque  (支票付款)</strong><br>
                            客戶於銀行設有支票戶口，並以支票方式繳交保費；或客戶申請保單提款、退保或退款等時，保險公司或會以支票方式支付給客戶。<br>
                            <br>
                            <strong>Claim  (賠償 / 索償)</strong><br>
                            受保人發生事故時，可根據保單條款向保險公司索償。（請留意保單列明的保障範圍）<br>
                            <br>
                            <strong>Client Zone  (客戶專區)</strong><br>
                            本公司客戶用於瀏覽所持有保單狀況的電子帳戶。<br>
                            <strong><br>
                                Collection Date  (扣款日期/過數日)</strong><br>
                            保險公司扣除保費的日期。<br>
                            <br>
                            <strong>Commencement Date  (保單開始的日期)</strong><br>
                            保單開始生效的日期， 詳情請參閱個別保險公司的保單釋義。<br>
                            <br>
                            <strong>Commission  (佣金)</strong><br>
                            保單成功生效後，保險公司根據與本公司/理財顧問之間的協議所支付給理財顧問的薪酬。<br>
                            <strong><br>
                                Common Reporting Standard - CRS  (共同匯報標準)</strong><br>
                            共同匯報標準（CRS）是經濟合作與發展組織所發展的國際標準，旨在通過參與稅務管轄區夥伴作出的全球性自動交換財務帳戶資料，以提高稅務透明度及提升稅務合規性。根據CRS，保險公司(及所有財務機構)必須辨識其稅務居民身份或不位於香港的客戶，並將相關保單資料申報至香港稅務局。而相關資料或會與客戶稅務居住地的稅務機關共用。<br>
                            <br>
                            <strong>Consultant Hotline  (理財顧問熱線)</strong><br>
                            熱線號碼為 +852 36013603，由本公司客服人員接聽，向理財顧問提供電話諮詢服務。<br>
                            <br>
                            <strong>Contribution Holiday  (保費假期)</strong><br>
                            最初供款期後客戶可申請暫停繳交保費。保單帳戶中須有足夠金額支付保單的管理費用，期間保單持續有效；每間保險公司設有不同期限。<br>
                            <br>
                            <strong>Cooling Off Cancellation  (冷靜期內取消保單)</strong><br>
                            保單已出單後，客戶在冷靜期內提出取消保單申請。<br>
                            <br>
                            <strong>Cooling Off Expiry Date  (冷靜期到期日)</strong><br>
                            冷靜期過後，如客戶取消保單，已繳保費將不能全數取回。<br>
                            <br>
                            <strong>Cooling Off Period  (冷靜期)</strong><br>
                            保單持有人可在保單發出後若干天內，取消已購買的保單，並取回原來已繳的保費。一般為保單發出的21天內，或保險公司向客戶或客戶的代表發出通知書後的21天內，以較先者為準。於「冷靜期」內，保單持有人有權取消保單。<br>
                            <br>
                            <strong>Correspondence Address  (通訊地址)</strong><br>
                            能有效接收郵寄物品的地址，可與居住地址相同，亦可不相同。<br>
                            <br>
                            <strong>Counter Offer  ( 修訂計劃)</strong><br>
                            客戶提交保險申請後，保險公司如對該申請有補充、改動或拒絕的部分，將反饋給客戶，客戶需接受保險公司的條款，申請才會繼續。<br>
                            <br>
                            <strong>Coverage/Cover  (承保範圍 / 保障範圍)</strong><br>
                            保單保障範圍的條款及規定，包括保單所提供的特定保障。<br>
                            <br>
                            <strong>Credit Card - CC  (信用卡付款)</strong><br>
                            客戶使用信用卡繳交保費。<br>
                            <br>
                            <strong>Credit Card Authorization - CCA  (直接付款授權-信用卡)</strong><br>
                            客戶授權銀行建立直接付款指示 ， 允許保險公司定期從客戶指定的信用卡戶口以自動轉賬方式繳交保費。<br>
                            <br>
                            <strong>Credit Card Expiry Date  (信用卡有效期)</strong><br>
                            信用卡的有效日期。<br>
                            <br>
                            <strong>Critical Illness  (危疾保險)</strong><br>
                            重大疾病保障計劃，保障範圍包括癌症等致命疾病。如受保人於保單有效期內確診，需向保險公司申請賠償。<br>
                            <br>
                            <strong>Currency  (貨幣)</strong><br>
                            計算保單價值所使用的貨幣單位。<br>
                            <br>
                            <span style="text-decoration: underline;"><strong>D</strong></span></p>
                        <p><strong>Death Benefit  (身故賠償)</strong><br>
                            受保人在保單有效期內身故，保險公司依照保險合同給付的死亡撫恤金。<br>
                            <br>
                            <strong>Death Certificate  (死亡證明)</strong><br>
                            證明受保人死亡的官方證明文件。<br>
                            <br>
                            <strong>Debit Date  (扣款日期 / 過數日)</strong><br>
                            保險公司扣除保費的日期。<br>
                            <br>
                            <strong>Declined  (被拒絕)</strong><br>
                            保單被保險公司拒絕承保而不能完成申請。<br>
                            <br>
                            <strong>Decrease Premium  (降低保費 / 減供)</strong><br>
                            客戶減低定期保費金額。<br>
                            <br>
                            <strong>Decrement  (降低保費 / 減供)</strong><br>
                            客戶減低定期保費金額。<br>
                            <br>
                            <strong>Direct Debit Authorization - DDA  (直接付款授權 - 銀行戶口)</strong><br>
                            客戶授權銀行建立直接付款指示， 允許保險公司定期從客戶指定的銀行戶口以自動轉賬方式繳交保費。<br>
                            <br>
                            <strong>Direct Fund  (直接基金)</strong><br>
                            保險公司運作其投資選擇的一種方式。在此方式下，投資選擇的投資比率、收費及費用和投資回報會與相關的相連基金一致。<br>
                            <br>
                            <strong>Dividend  (紅利)</strong><br>
                            "當保單已生效了一段指定的時間，在保單有效期間內，或於保單取消/終止或支付賠償時 (例如﹕身故或危疾)派發的非保證金額。<br>
                            週年紅利是保險公司決定派發之可分配盈餘，在保單週年日派發，紅利分配之多寡，需視乎保險公司當年的整體業務表現和投資回報率釐定，金額不獲保證。紅利領取方法包括有積存生息、領取現金、抵付保費或購買繳清保險。"<br>
                            <br>
                            <strong>Dormancy Charge  (長期停止供款保單費用)</strong><br>
                            Zurich Vista3保單用詞，指保單變為長期停止供款保單後保險公司所收取的一筆不退還的費用。<br>
                            <br>
                            <strong>Dormant  (帳戶休眠 / 長期停止供款保單)</strong><br>
                            Zurich Vista3保單用詞，指保單在三年保費假期後，如客戶不進行保單復效，該保單帳戶將會變為長期停止供款保單，保險公司亦會扣除一筆費用(即Dormancy Charge)。<br>
                            <br>
                            <strong>Due Date  (保費到期日)</strong><br>
                            保費欠款到期日，該日期前保費須繳清。<br>
                            <br>
                            <strong>Duplicate Policy  (保單副本)</strong><br>
                            客戶遺失保單正本，要求保險公司重新印刷一份保單副本作為保存。一般情況下，保險公司會收取保單重發費用。<br>
                            <br>
                            <strong><span style="text-decoration: underline;">E</span></strong></p>
                        <p><strong>Effective Date  (生效日期)</strong><br>
                            保單開始生效的日期，保險公司開始提供保障。<br>
                            <br>
                            <strong>Employee Account  (僱員帳戶)</strong><br>
                            僱主為僱員開立的強積金帳戶。<br>
                            <br>
                            <strong>Employer Account  (僱主帳戶)</strong><br>
                            僱主為僱員購買強積金的帳戶。<br>
                            <br>
                            <strong>Encashment  (兌現/提款)</strong><br>
                            指保單過了最初供款期後，客戶可以部分提取保單價值（一般為累積供款帳戶內的價值，不可提取最初供款期內的價值）。<br>
                            <br>
                            <strong>Encashment History  (提款紀錄)</strong><br>
                            客戶曾經部分提款的歷史紀錄，通常包括金額、時間等。<br>
                            <br>
                            <strong>Encashment Value  (可提取價值)</strong><br>
                            客戶可提款部分的保單價值。<br>
                            <br>
                            <strong>Endorsement  (批註)</strong><br>
                            除一般條文外，保單上任何附加的文字。為適應實際需要而補充或變更保單內容之條款。附加的條文通常會加貼於保單上，或附註於保單空白處。任何批註都須由保險公司主管人員簽署，並附於保單上，成為保單之一部份，才具有效力。<br>
                            <br>
                            <strong>Entry Proof  (入境證明)</strong><br>
                            外國居民(包括中國內地客戶)前往香港的入境證明。<br>
                            <br>
                            <strong>e-Statement  (電子結單)</strong><br>
                            現時，電子結單以月結形式發出，大致可分為三類: 分別是 Fund、Bond和ILAS產品。當電子結單發出後，客戶將會收到電郵或SMS通知，客戶可登入「客戶專區」(Client Zone) 以檢閱電子結單。<br>
                            <br>
                            <strong>Exclusion  (不保事項)</strong><br>
                            保單普遍均附有不保事項條款，列明在一些指定情況下，保險公司將不會承擔其風險及不會給予賠償。<br>
                            <br>
                            <strong>Ex-Date  (除息日)</strong><br>
                            指一個特定的日期，如果某一上市公司宣佈派發紅利股份、紅利認股權證、以折讓價供股或派發其他有價權益，在除息日之前一日持有它的股東可享有該權益，在除息日當日或以後才買如該公司股票的人則不能享有該等權益。<br>
                            <br>
                            <strong>External Transfer In  (外部轉讓)</strong><br>
                            客戶將保單由其他保險中介公司轉入，由理財顧問管理此客戶保單的行為。<br>
                            <br>
