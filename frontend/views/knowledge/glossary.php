                            <br>
                            <strong>Outstanding Premium  (逾期保費)</strong><br>
                            逾期未繳的保費。<br>
                            <br>
                            <strong>Overdue Premium  (逾期保費)</strong><br>
                            逾期未繳的保費。<br>
                            <br>
                            <strong>Owner  (保單持有人 / 投保人)</strong><br>
                            購買保險的人，擁有保單合同內的各種權利。保單持有人 / 投保人可以與受保人和受益人是同一人，也可以是其他任何人。<br>
                            <br>
                            <strong><span style="text-decoration: underline;">P</span></strong></p>
