                            由國際標準化組織（ISO）製定的證券編碼標準，ISIN編碼是全球通用的、用於識別各個國家或地區證券的唯一代碼，主要用於證券的跨境交易和結算，該編碼由12位元數位或字母組成。<br>
                            <br>
                            <strong>Investment Linked Assurance Scheme - ILAS  (投資相連壽險計劃)</strong><br>
                            投資相連壽險計劃兼備人壽保障及投資成份。客戶既可享有身故賠償，亦可將部份保費投資於指定的基金，以獲取潛在回報。<br>
                            <br>
                            <strong>Issue Date  (簽發日期)</strong><br>
                            保單簽發 / 出單的日期。<br>
                            <br>
                            <strong>Issued  (保單出單 / 保單發行)</strong><br>
                            保單已出單的狀態，將會正式生效。<br>
