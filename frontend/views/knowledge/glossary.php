                        <p>
                            <strong>ID Proof  (身份證明文件)</strong><br>
                            一般指身份證或護照（包括姓名、出生日期、地址和身份証號碼等）<br>
                            <br>
                            <strong>Illustration  (建議書 / 計劃書 / 銷售說明)</strong><br>
                            理財顧問根據客戶的保險需求所制定出的利益說明建議書，概括保單的預計現金價值及身故賠償等。<br>
                            <br>
                            <strong>Important Facts Statement and Applicant's Declarations  (重要資料聲明書及投保人聲明書)</strong><br>
                            客戶申請投資相連壽險計劃時，需提交《重要資料聲明書/投保人聲明書》，以確保保險公司已經向客戶提供足夠及清晰的資料，包括產品的主要特點及風險等。<br>
                            <br>
